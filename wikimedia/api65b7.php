==Move List Primer==&lt;br /&gt;
&lt;br /&gt;
First of all you should familiarize yourself with the [[Glossary|abbreviations]] we use throughout the site. Over the years these have become pretty much the standard for Tekken webmasters and FAQ writers. Each of the Move List columns contain specific move information.&amp;lt;br /&amp;gt;&lt;br /&gt;
&amp;lt;br /&amp;gt;&lt;br /&gt;
'''Command Column'''&amp;lt;br /&amp;gt;&lt;br /&gt;
Pretty self-explanatory. This is how you execute the listed move. The directions are in relation to which way your character is facing, of course.&amp;lt;br /&amp;gt;&lt;br /&gt;
&lt;br /&gt;
'''Type Column'''&amp;lt;br /&amp;gt;&lt;br /&gt;
This applies only to the Grappling Arts. It indicates your position in relation to the opponent. Most Multi Parts Arts have to be done from the front. Grabbing with a Multi Part starter from the side or back will result in a regular back or side grab in most cases.&amp;lt;br /&amp;gt;&lt;br /&gt;
&lt;br /&gt;
'''Name Column'''&amp;lt;br /&amp;gt;&lt;br /&gt;
This information is trivial. These are not all the official move names from Namco by the way. Tekken players started naming move long before Namco ever did, and a lot of them just stuck.&amp;lt;br /&amp;gt;&lt;br /&gt;
&lt;br /&gt;
'''Damage Column'''&amp;lt;br /&amp;gt;&lt;br /&gt;
Tekken damage is based on points, not percentages. The Tekken Tag 2 lifebar is 180 points for both tag vs tag and solo vs solo. For tag vs solo, it will be 170 points for tag and 230 for solo. Standing hits do 135% of their base value on tag, vs tag and tag vs solo, while it's 120% on solo vs solo matches. We have opted to populate the lists with the actual damage output because it seems to make more sense to know what it actually does and not what it is based of.&amp;lt;br /&amp;gt;&lt;br /&gt;
&lt;br /&gt;
'''Escape Column'''&amp;lt;br /&amp;gt;&lt;br /&gt;
Another Grappling Arts only column. It tells you which escape to use for a particular throw. More detailed escape information can be found in the system sections. Complex escapes such as mounts and tackles are usually labeled as 'system' since there are various escape options and windows.&amp;lt;br /&amp;gt;&lt;br /&gt;
&lt;br /&gt;
'''Hit Range Column'''&amp;lt;br /&amp;gt;&lt;br /&gt;
Indicates at which level a listed move hits. Tekken has 4 major hit ranges; take a closer look at the [[glossary]] for more detailed information. These are based on a standard size character on even ground away from walls. This especially relates to ground hitting moves of which properties can change going uphill, near walls or on overly large characters&amp;lt;br /&amp;gt;&lt;br /&gt;
&lt;br /&gt;
'''Properties Column'''&amp;lt;br /&amp;gt;&lt;br /&gt;
Contains a wide variety of extra move information, such as stuns, staggers and much more. Be sure not to overlook the footnote indicators.&lt;br /&gt;
