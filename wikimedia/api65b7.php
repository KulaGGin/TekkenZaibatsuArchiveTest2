== Getting Started ==&lt;br /&gt;
&lt;br /&gt;
===Observe Copyrights===&lt;br /&gt;
Write articles and content yourself, or copy it from a public domain or similar free resource. Do not submit copyrighted work without permission!&lt;br /&gt;
&lt;br /&gt;
===Play Nice===&lt;br /&gt;
When contributing to this Wiki please note that all contributions may be edited, altered, or removed by other contributors. That being said, don't scribble all over someone's page. The wiki has built-in tools to help authors protect their content and go back in history, but that does mean you should be trashing up the place.&lt;br /&gt;
&lt;br /&gt;
===Maintain a Consistent Style===&lt;br /&gt;
Ensure your contributions are in an appropriate format. You can use the various [[Tekken Tag Tournament 2]] sections and pages as a guide line. Also check The Wiki Project forum to discuss the layout and desired format.&lt;br /&gt;
&lt;br /&gt;
===Need Some Help===&lt;br /&gt;
If you've never used a wiki before, the MediaWiki [http://meta.wikipedia.org/wiki/MediaWiki_User%27s_Guide User's Guide] contains lots of information for usage and configuration help.&amp;lt;br /&amp;gt;&lt;br /&gt;
&amp;lt;br /&amp;gt;&lt;br /&gt;
If you'd like to jump right in, editing a Wiki is lot like writing a forum post, except the markup rules are much more rich.  You can check the [http://meta.wikimedia.org/wiki/Help:Editing Editing] section on MediaWiki for a description of all the rules available, with some brief examples.&lt;br /&gt;
